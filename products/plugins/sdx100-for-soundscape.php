      <p><span class="style6"><span class="style5">SDX100 <span class="style5BLUE">- for Soundscape</span></span></span></p>
      <p> <span class="style1">The SDX100 is a mixer plug in compatible with all Soundscape Digital Audio Workstations</span> (Soundscape 32, Soundscape 16, R.Ed, SSHDR1-Plus) and DSP powered expansion cards (Mixpander, Mixtreme) that brings the legendary quality and character of Drawmer compressors to the Soundscape platform.</p>
      <p>&nbsp;</p>
      <p>The SDX100 features many well-known Drawmer innovations such as Frequency Conscious Noise Gating, Program Adaptive Expansion, &quot;Bootstrap&quot; Compression and Zero Overshoot Limiting. With mono and stereo versions with or without key triggering and a choice of Noise Gate or Expander, The SDX100 is versatile enough to be used as either a channel dynamics package or a mixdown processor, and all its components can be used as a group or individually.</p>
      <p>&nbsp;</p>
      <p>The SDX100's clearly organized graphical interface provides a high degree of real-time visual feedback, with clever touches, such as the threshold cursors that move alongside the meters, making it extremely easy to set up. Its sonic characteristics ensure first-class results with any type of source material.</p>
      <p>&nbsp;</p>
      <p>The SDX100 has four main components:<br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Compressor</strong><br />
        Full band program adaptive compressor, switchable between normal and &quot;bootstrap&quot; operation with threshold, ratio, switchable knee characteristics, attack and release (switchable between manual and auto modes), and gain make-up controls.</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Limiter</strong><br />
        Variable threshold limiter with five switchable release times.</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Gate</strong><br />
        Frequency conscious noise gate featuring sweepable HF and LF key filters, key listen switch, L, R, L+R and external triggering options, threshold control with easy set pointer and threshold meter, traffic light status indicators, full envelope control with attack, hold, release and adjustable range.</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Expander</strong><br />
        Full band expander with threshold, ratio, attack, release, range controls also  a threshold meter.</p>
      <p>&nbsp;</p>
      <h3>Main Features:</h3>
      <ul class="feature">
	      <li>Drawmer compression.</li>
	      <li>Frequency Conscious Noise Gating.</li>
	      <li>Program Adaptive Expansion.</li>
	      <li>&quot;Bootstrap&quot; Compression.</li>
	      <li>Zero Overshoot Limiting.</li>
	      <li>Sidechain allows ducking/de-essing.</li>
