      <p><span class="style6"><span class="style5">Tourbuss <span class="style5BLUE">- for Digidesign Venue</span></span></span></p>
      <p><span class="style1">Designed with the live sound engineer in mind</span>, Drawmer TourBuss offers four dynamics plug-ins exclusive to Digidesign VENUE systems. The Drawmer TourBuss suite includes a Noise Gate, Compressor/Limiter, Expander, and Bracketing Filter, each accessible as individual, stand alone effects and able to patch into the Venue insert points or sends anywhere along the audio signal chain and in any order, and all retaining the layout and emulate the sound of Drawmer&rsquo;s renowned DS201 Noise Gate and DL241 Auto-Compressor hardware units so the live engineer should feel completely at home when using them.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Noise Gate</strong><br />
        The Noise Gate features variable hi- &amp; lo-pass filters to achieve frequency-specific gating while comprehensive envelope controls enable you to fine-tune attack, hold, decay, and range. With a &ldquo;traffic-light&rdquo; gate status display and metering that shows you when the threshold is being approached, you can see precisely how the gate is interacting with a channel&rsquo;s program content. Using the Noise Gate in side-chain mode, a &ldquo;key listen&rdquo; switch enables you to quickly hear the audio source that is controlling the gate.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Compressor/Limiter</strong><br />
        Combining both ratio-style and soft-knee compressors, the TourBuss Compressor/Limiter is equally adept at creative compression work and unobtrusive level control, all with analog-style response. It incorporates a wide threshold range, a fully adjustable ratio (from gentle to absolute limiting), switchable soft/hard knee, and an optional compressor bypass for limiter-only use. An automatic program-adaptive attack and release help to provide trouble-free setup while an LED-style display shows gain reduction.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Expander</strong><br />
        With easy set-up and advanced features  that are designed to overcome problems encountered by typical expanders the TourBuss Expander is the perfect solution when you need an efficient way to raise the volume of wanted sounds that are only slightly above the noise floor.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Bracketing Filter</strong><br />
        With low and high 12 dB&ndash;per-octave filters, the bracketing filter removes unwanted frequencies that bleed into the source without having to use a more complicated, resource-hungry equalizer.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>VENUE Integration</strong><br />
        The Drawmer TourBuss suite integrates easily into the VENUE D-Show&trade; console and DSP mix engine. Because many live sound engineers already have experience using Drawmer analog gates and compressors, most will find the TourBuss suite an easy-to-use and essential addition to the VENUE environment.</p>
