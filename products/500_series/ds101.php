      <p><span class="style6"><span class="style5">DS101 <span class="style5BLUE">- Noise Gate for the 500 Series</span></span></span></p>
      <p> <span class="style1">The Industry-Standard High-End Noise-Gate comes to the 500 Series!</span></p>
      <p> The DS101 is the first noise gate designed specifically for the 500 Series rack system and takes its place alongside the Drawmer family of industry standard Noise Gates, such as the DS201, DS501 and DS404. Anyone familiar with those units will immediately be at home with the DS101.</p>
      <p>&nbsp;</p>
      <p>Based on a single-channel of the DS201 the DS101 is a sophisticated noise gate incorporating a number of features pioneered by Drawmer, which are invaluable to the sound engineer, such as variable high and lowpass filters for “frequency conscious gating.”</p>
      <p>&nbsp;</p>
      <p>Other features include comprehensive envelope control, attack, hold, decay, and range, plus a whole host of other features, all adding up to one amazing processor.</p>
      <p>&nbsp;</p>
      <p>When placed side by side in a 500 series rack two or more gates can be linked using the new infra red triggering mechanism employed on the DS101. This means that any number of gates can be linked with each having their own envelope shaping whilst the trigger pulse passes through unchanged, this opens up a whole world of “envelope follower” effects limited only by your imagination.</p>
      <p>&nbsp;</p>
<img src="../../images/ds101_rack.jpg" alt="DS101 Rack" />
      <h3>Features:</h3>
      <ul class="feature">
	      <li>Fully featured noise gate based on the industry standard DS201.</li>
	      <li>Takes up a single 500 series slot.</li>
	      <li>Proprietary Hysteris or “trigger stabilization” to prevent “chatter” around the threshold.</li>
	      <li>Fully variable high pass and low pass filters for “frequency conscious” gating.</li>
	      <li>Comprehensive envelope control, attack, hold, decay, and range.</li>
	      <li>External triggering via key input or the new IR trigger pulse from another DS101.</li>
	      <li>“Key Listen” facility.</li>
	      <li>Extremely fast attack time, to preserve the natural attack of the sound.</li>
	      <li>Linkable - up to ten units in one rack space - with individual envelope shaping.</li>
          <li>Drawmer “traffic lighting” metering.</li>
          <li>Can be used for “Gating” or “Ducking”.</li>
          <li>Low noise design.</li>
      </ul>
      <p>&nbsp;</p>
      <p>Setting up noise gates can be tedious and unrewarding. For example, when attempting to separate a signal from unwanted noise or crosstalk that is relatively high in level, spurious triggering of the gate by unwanted components within the sound can be a serious problem. This can often be experienced in the studio when recording a drum kit. If a gate is used to clean up the snare drum sound it is quite likely that the nearby hi-hats will spill into the snare drum microphone and cause the gate to open. Increasing the threshold level may cure this problem, but then there is a very real danger that any quieter snare drum beats may not cause the gate to open at all and the performance can easily be ruined.</p>
      <p>&nbsp;</p>
      <p>The DRAWMER solution to this problem is the inclusion of two variable filters, one high-pass and one low-pass, which act upon the side-chain keying circuitry. By setting the output switch to key listen the user can hear the action of the filters and adjust them to reject high frequency spillage from the hi-hats. This now enables the gate to only open on the lower frequencies present in the snare drum.</p>

      <p>&nbsp;</p>
      <p>The very fast attack of the DS101 means that it can open in a matter of micro seconds, thus preserving the natural attack of whatever sound is being gated and the comprehensive envelope controls mean that the gain can be changed at whatever rate best suits the material being processed. With vocals for example, a fairly fast attack is needed so as not to clip the leading consonants but a slower release time will prevent the end of words being clipped off and will also fade out any noise gradually rather than cutting it off abruptly. This latter point is very important as the human ear is far more sensitive to rapidly changing noise levels than to a constant low level noise.</p>
      <p>&nbsp;</p>
      <p>Another important role played by the gate is in the reshaping of existing sounds. An example of this might involve a simple sampling delay line used to store a sound to be re-triggered later and added to a mix. A gate can be used to impart a slower attack or faster decay to the sound and careful setting of the decay envelope can effectively hide any noise present at the end of the sample. When using a sampler or drum machine the gate can be considered a triggered envelope shaper, and the wide range allowed by the envelope controls make the DS101 ideal for this application.</p>
      <p>&nbsp;</p>
      <p>The unit can be switched to accept a key input, which allows the gate to be triggered externally. An example of this would be to use a snare drum signal to open the gate on a separate ambient microphone to create a natural alternative to gated reverb.</p>
      <p>&nbsp;</p>
      <p>With a rack full of DS101's each can be individually switched from Gating to Ducking for 'voice over' applications or the removal of 'clicks' and 'pops'.</p>
