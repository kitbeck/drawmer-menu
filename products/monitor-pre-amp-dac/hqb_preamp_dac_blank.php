      <p><span class="style6"><span class="style5">HQ-b <span class="style5BLUE">- Precision Pre-Amplifier / DAC Blank Module</span></span></span></p>
      <p><span class="style1"> </span>The HQ-b expands on the HQ by offering a product that is identical in every way to the HQ, incorporating the exact same function a specifications, but has been designed to be controlled remotely, either from an HQ or the HQ-r remote and so lacks the controls of the HQ front panel. It is installed in the same way, has identical power and audio connection on the rear, and the same performance.</p>
      <p>&nbsp;</p>
      <p>The HQ-b incorporates a linking system (via standard cat5e cables) that provides an easy means of building a monitor control/pre-amp system incorporating multiple HQ units. Several units can be linked together, to provide a distribution system, or a 5.1 / 7.1 surround setup with only one master control. </p>
      <p><br />
      </p>
      <p><strong>The Controls</strong>:<br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>POWER</strong>:<br />
        A rotary switch that turns the HQ-b on or off.  When on the soft start procedure will begin - this will take a few seconds, during which time the HQ-b will not pass audio. Note that it is a hard powered switch, not standby, and so when in the off position the HQ will draw no mains power.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>HEADPHONES</strong>:<br />
        Two headphone sockets are provided that supply both headphones simultaneously. The volume is controlled by the main volume control level, in the same way as the analogue outputs. As factory standard the right headphone socket will mute these main outputs when plugged in, whilst the left socket leaves the outputs as is.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>MUTE</strong>:<br />
        The mute led will be lit whenever the relay attenuation volume control is prevented from passing audio.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>REMOTE</strong>:<br />
        Whenever a RJ45 cable is plugged into the &lsquo;In/Remote&rsquo; socket on the rear of the unit the &lsquo;Remote&rsquo; LED will light to show that the unit is being controlled elsewhere.</p>
      <p>&nbsp;</p>
      <p>All other controls are performed remotely.</p>
