      <p><span class="style6"><span class="style5">MX30-Pro <span class="style5BLUE">- Dual Gated Compressor Limiter</span></span></span></p>
      <p><span class="style1"> </span>The <span class="style1">MX30 Pro Gated/Comp/Limiter</span> is a low cost professional quality dynamics processor. Drawing on our many years experience in professional audio we have developed a series of lower-cost Drawmer dynamic signal processors, the MX Pro range. Aimed at the musician, project studio and live performer, who is determined not to compromise on quality, but have to work to a tighter budget.<br />
        By using circuit elements drawn from existing top-of-the-line Drawmer products and by simplifying the control systems where possible, the MX Pro range is no less sophisticated than our other units, despite the price tag. As a result the inexperienced user gains the benefits of Drawmer quality and performance, combined with lower cost and simplicity of operation.</p>
      <p>&nbsp;</p>
      <p>Operational control is split into three sections:- Gate, Compressor, Output:</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>&lsquo;Programme Adaptive&rsquo; Gate</strong><br />
        Used to remove unwanted noise below a pre-set level, each channel consists of a variable Threshold Gate incorporating Drawmer&rsquo;s &lsquo;Programme Adaptive&rsquo; circuitry. Although quick and simple to operate, the gate&rsquo;s auto-circuitry continually optimises such parameters as Attack, Hold, Ratio and Release, completely eliminating &lsquo;chatter&rsquo; when the signal is fluctuating on or around Threshold. Two LEDs show when the signal is &lsquo;above&rsquo; or &lsquo;below&rsquo; Threshold and a push button switch allows additional control of Release time. This design approach also retains all &lsquo;leading edge&rsquo; information making the gate ideal for &lsquo;cleaning up&rsquo; vocals or retaining the &lsquo;punch&rsquo; on gated drums. Switching is provided for 'Fast' or 'Slow' release characteristics. The 'Fast' mode can be used for instruments with rapid decay times or for additional tightening of the gating effect. The 'Slow' mode is more suitable for vocals and instruments with longer decay times or can be used to smooth over rough fade outs. A single LED indicates gating action.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Soft Knee &lsquo;Auto Compressor&rsquo;</strong><br />
        The gate is followed by a variable Threshold, variable Ratio, soft knee compressor with fully automatic Attack and Release. Large transients are controlled instantly whilst small signal variations are treated gently, retaining as much &lsquo;life&rsquo; as possible and without generating unpredictable side effects. the MX30 Pro handles all compression levels with ease and displays the amount of compression  on a high resolution Gain Reduction bargraph meter.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Output</strong><br />
        The Gain control is used to compensate for the amount of compression and brings the signal to the required output level as displayed by the output bargraph meter. A variable Threshold &lsquo;zero overshoot&rsquo; limiter, essential for avoiding distortion when mastering to digital or speaker protection in a &lsquo;live&rsquo; situation, adds the final polish to the signal by keeping control over signal peaks. The Limit threshold provides an absolute maximum peak level which no signal can pass. Fast transients are clipped instantly whilst more sustained peaks are dealt with by applying Gain Reduction, indicated by a single LED.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Gate Threshold:</strong></p>
      <p>This control sets the level below which gating takes place and may be set anywhere in the range of -70dB to +20dB. A green LED illuminates to show when the signal is above threshold (i.e. gate open) and a red LED illuminates when the signal is below Threshold (i.e. gate closed).</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Release:</strong> Either a Fast or Slow auto release contour may be selected depending on the material being processed. An LED indicates when Slow release status is engaged. Percussive material with little or no reverb is generally treated using the Fast release setting, whereas with slow decays or a significant amount of added reverb will usually respond better to the Slow release setting.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Compressor Threshold:</strong> This determines the input level above which gain reduction will be applied and may be set anywhere in the range of -40dB to +20dB. Soft Knee compression takes place for signals exceeding the threshold level by up to 10dB above which level, conventional &lsquo;ratio&rsquo; compression is applied.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Gain Reduction Bargraph Meter:</strong> A nine segment LED bargraph meter continuously monitors the gain reduction applied by the compressor over a range of 0dB to 30dB.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Ratio:</strong> Sets the final compression ratio that will be applied once the 10dB soft knee region is exceeded. The ratio may be continuously adjusted from 1.2:1 to infinity:1 allowing the possibility of true hard limiting. The higher the ratio setting the more compression takes place (eg. A ratio of 8:1 means that an 8dB increase in volume at the input will appear as a 1dB increase in volume at the output).</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Stereo Link:</strong> The unit can function as two independent gated/compressor/limiters or when &lsquo;link&rsquo; mode is engaged for stereo use Channel 1 becomes the master, automatically duplicating its parameters across to Channel 2. In this mode the same degree of gain reduction is applied to both audio channels to prevent image shifting.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Output Gain:</strong> The output level may be attenuated or amplified by up to 20dB to compensate for level changes caused by compression. This control comes directly before the Peak Limiter detector and this fact should be taken into account when setting the Peak Limiter threshold.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Peak Limit:</strong> This control sets an absolute limit that the output signal will not be permitted to exceed. The limiter is &lsquo;zero response time&rsquo; and controls peaks without any audible distortion. For transparent &lsquo;peak protection&rsquo; the output gain control should be set to ensure that the Peak Limiter operates only occasionally. Alternatively, the unit can be driven into heavy limiting for creative effects.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Input/Output Bargraph Meter:</strong> An eight segment LED bargraph level meter monitors the output signal level over a range of -20dB to +15dB. When Bypass is selected this meter reads input level.</p>
