      <p><span class="style6"><span class="style5">MX50-Pro <span class="style5BLUE">- Dual Vocal De-Esser</span></span></span></p>
      <p><span class="style1"> </span>The human voice is one of the most diverse signals ever encountered by engineers because it contains such large variations in level and harmonic structure. Getting the desired vocal sound can sometimes prove difficult. Setting the E.Q. to give the vocal sufficient 'definition' and 'cut' in the mix can cause large sibilant peaks which often lead to severe distortion. The MX50 Pro 'Vocal' De-Esser solves these problems quickly and efficiently by reducing the level of the sibilant sounds whilst leaving the remainder of the signal unchanged.</p>
      <p>&nbsp;</p>
      <p>The <span class="style1">MX50 is a flexible, simple to set up dual De-Esser</span> designed to fulfil the needs of the Recording Studio, Broadcast, Theatre and Live Sound Industries. Its classic application is to overcome the problems that can arise when sibilant sounds (S's and T's) are over-emphasised by the human voice, with such other applications as 'smoothing' a stereo mix, reducing string noise on acoustic guitars or breath noise on flutes, even refining cymbal and hi-hat sounds. Processing is achieved by adjusting just two control parameters, Frequency and De-Ess. The fully variable Frequency control operates from 800Hz to 8KHz encompassing the full sibilant range, including 'hard' sounding harmonics and other side-effects which can be generated when treble frequencies are boosted or enhanced. This is followed by the variable De-Ess which acts as a 'more' control giving up to 20dB of gain reduction at the selected frequency.</p>
      <p>&nbsp;</p>
      <p><strong>The MX50 Pro has two modes of operation:</strong><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Split Band de-essing:</strong> Recommended for the majority of applications, this mode splits the audio spectrum into two bands with the split point being dictated by the position of the Frequency control. Only frequencies above the split point are attenuated.<br />
       <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Full Band de-essing</strong>: A useful alternative where unobtrusive minimal processing is required. This mode reduces the total signal level in response to sibilant peaks in the selected frequency band.</p>
      <p>&nbsp;</p>
      <p>Main Features and Controls:<br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Floating Threshold System</strong>: This intelligent Drawmer circuitry has a floating Threshold that adapts to the dynamics and level of the incoming material, eliminating the need for a manual Threshold control and creating more natural results.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Air function</strong>: As sibilance rarely occurs above 10KHz this feature adds back high frequency 'gloss' in excess of 12KHz preserving upper harmonics and increasing the transparency of operation.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Frequency</strong>: This control selects the frequency band in which the de-esser operates. In Split Band mode this control also determines the split point above which gain reduction will take place. The control is continuously variable from 800Hz to 8KHz and covers the full sibilant range.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>De-Ess</strong>: Adjusts the amount of gain reduction that will take place when a sibilant sound has been detected. Up to a maximum of 20dB may be applied. This control uses an intelligent Floating Threshold circuitry which removes the need for a manual De-Ess Threshold control. One of the benefits of this design approach is that during quiet passages the sibilants are held to a level which is relative to the original signal.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Split Band De-Ess</strong>: Split Band operation will only reduce the selected frequencies above the value set via the Frequency control. In most situations this mode, in conjunction with the Air function, offers the most transparent de-essing. Status LED illuminates when engaged.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Full Band De-Ess</strong>: Full Band operation will reduce the total signal level in response to signals in the selected frequency band. Status LED illuminates when engaged.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Air</strong>: The Air control enables frequencies above the sibilant band to be retained, greatly increasing transparency of the de-essing process. During normal use it is recommended to keep the Air function active for both Split Band and Full Band de-essing. Status LED illuminates when engaged.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Link</strong>: Depressing this switch configures the unit for stereo operation where the average left/right signal level is used as the Floating Threshold. In linked mode the red status LED beneath the link switch will illuminate.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>De-Ess Gain Reduction Display</strong>: A nine segment LED bargraph shows how much gain reduction is being applied up to a maximum of 20dB. In Full Band mode gain reduction is applied to the whole signal, whilst in Split Band mode only frequencies above the value set on the frequency control are subject to gain reduction.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Bypass</strong>: The Bypass position routes the input signal to the output with no processing. These switches are independant of any linking and will only bypass their respective channel. Status LED illuminates when engaged.</p>
