      <p><span class="style6"><span class="style5">MX40-Pro <span class="style5BLUE">- Four Channel Punch Gate</span></span></span></p>
      <p> <span class="style1">The MX40 Pro Punch Gate is a high performance, simple to use 1U four channel noise gate</span> that offers the same extraordinary sonic character, ingenuity, and high technical specification that have made Drawmer the world standard in gating technology. The MX40 Pro is the first low cost, truly professional gate to offer the features and performance typical of much more expensive noise gates.</p>
      <p>&nbsp;</p>
      <p>Drawing on our many years experience in professional audio we have developed a series of lower-cost Drawmer dynamic signal processors, the MX Pro range. Aimed at the musician, project studio and live performer, who is determined not to compromise on quality, but have to work to a tighter budget. By using circuit elements drawn from existing top-of-the-line Drawmer products and by simplifying the control systems where possible, the MX Pro range is no less sophisticated than our other units, despite the price tag. As a result the inexperienced user gains the benefits of Drawmer quality and performance, combined with lower cost and simplicity of operation.</p>
      <p>&nbsp;</p>
      <p>The MX40 Pro features proprietary 'Peak Punch' circuitry which adds unique dynamic enhancement to the leading edge of percussive material, adding true 'punch' to the performance. The MX40 Pro also includes Drawmer's Frequency Conscious System (first introduced with the DS201 Dual Gate), which 'tunes' the gate to react only to a specific frequency band, dramatically improving gate operation. The MX40 Pro packs four channels of high power Drawmer gating into a single rack space, with controls and indicators designed to make set up faster and easier.</p>
      <p>&nbsp;</p>
      <p>The Drawmer MX40 Pro Punch Gate offers improved signal definition in complex mixes by isolating desired signal from unwanted background noise. It is especially important in sound reinforcement and the recording of short term sounds such as percussion. The MX40 Pro Punch Gate Frequency Conscious System 'opens' the gate only when a signal exceeds the threshold you set within a specific and variable frequency band. This enables the gate to distinguish between a snare and a high hat, tom-toms versus bass drum etc. Drawmer's new Peak PunchTM circuitry further improves definition by dynamically enhancing the leading edge of transient signals immediately after gate opening, so percussive sounds 'punch' through the mix. The controls of the MX40 Pro are designed to reduce typical lengthy and complex gate set up. Only three rotary controls are needed per channel: 'Frequency Trigger' sets the frequency band the gate reacts to; 'Threshold' sets the level at which the gate opens; 'Release' determines how long before the gate closes after falling below the set Threshold level.</p>
      <p>&nbsp;</p>
      <p>Push button options include Peak Punch On/Off, 'Frequency Trigger' On/Off, -20dB or -90dB Attenuation and Gate Bypass. The industry standard Drawmer red/yellow/green 'traffic light' LED display gives the user reliable indication of gate status. Special features include 'Key Listen' that enables the user to hear just the trigger signal for easier set-up. If Frequency Conscious operation (Frequency Trigger &quot;On&quot;) is used, depressing Key Listen will enable the user to hear only the frequency band 'tuned' in on the Frequency Trigger rotary control. External Key allows the gate to be opened by an external signal. The MX40 Pro can also be set so Channel One triggers all other gate channels, and 'Slave Link' links Channel One and Two together and/or Three and Four.</p>
      <p>&nbsp;</p>
      <p><strong>Controls:</strong><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Threshold</strong>: This rotary control sets the level at which the gate opens (or closes) and may be set anywhere between -60dB to +20dB. All signals below Threshold will be reduced by -20dB or -90dB, depending on the 'Range' control setting. All signals above threshold will pass through the gate.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>LED Display</strong>: Drawmer's familiar traffic light LEDs display envelope status. When the gate is closed (signal below the set Threshold level) - the red LED is illuminated. When the gate is open (signal above the set Threshold level) - the green and amber LEDs illuminate and the red LED is off. As the signal falls below the set Threshold level, the green LED will extinguish and the amber LED fades over the duration of the Release time.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Release</strong>: The Release time may be set from 10mS to 4S. This control determines how long before the gate closes. Special logic circuits within the MX40 Pro incorporate 'envelope hold' constants that are variable with Release time settings. This is to prevent 'chatter' when processing signals that decay near Threshold.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Range</strong>: This button determines how much gain reduction is applied when the gate is fully closed. A setting of -90dB effectively silences the signal completely when the gate is closed, while the -20dB setting allows the signal to pass through the gate with a 20dB decrease in gain.</p>
      <p><br />
       <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Peak Punch</strong>: Drawmer's proprietary Peak PunchTM circuitry adds very short term dynamic expansion to the leading edge of percussive material. Peak PunchTM has a near zero Attack time and enables gated signals to 'punch' through a mix without increasing long term signal level. The effect is one of greater signal definition of transient information. When Peak Punch is active an LED below the switch illuminates.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Bypass</strong>: This enables individual channel Bypass for A/B comparison. The Bypass position routes the input signal to the output with no processing and is effectively a gate 'on/off' button. When in Bypass mode an LED below the switch illuminates.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Trigger Frequency</strong>: The Trigger Frequency filter is variable from 50Hz to 8Khz and enables the user to specify a frequency band that the gate 'listens to', causing it to open or close. This feature makes the MX40 Pro 'frequency conscious' and enables the gate to react only to signals within a one octave band. The centre of that octave is determined by the frequency centre of the rotary Trigger Frequency control. This allows fast and simple set-up of frequency selective operation where unwanted signals are prevented from triggering the gate.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Key Listen</strong>: Engaging this switch will enable the user to hear only that channel's trigger, or 'key'. The trigger can be the selected frequency band on the Trigger Frequency rotary control, or an external (key) signal. This makes set-up much faster and easier as you can 'tune' the gate while in Key Listen mode. An LED below the switch illuminates when in Key Listen mode.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Ext Key</strong>: Engaging this switch will offer a choice of two functions. The gate will be opened by either: (1) An external key source (if an external key source is plugged into the Key Input jack socket on the rear panel). (2) Channel One (if no key source is plugged into the rear panel). This makes it possible for selected gate channels to be opened by a single source. An LED below the switch illuminates when External Key is selected.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Filter In</strong>: This activates or deactivates the Frequency Conscious feature of the MX40 Pro, and activates the Trigger Frequency control. If filter is bypassed (off), then the gate threshold is full bandwidth.</p>
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Slave Link</strong>: The Link buttons are located between odd/even channels, and when depressed cause the channel on the right (Channel 2 or 4) to be controlled by the left-hand channel (Channel 1 or 3). In linked mode, the red status LED beneath the Slave Link switch will be illuminated, and the Ext Key and Peak Punch LEDs of the slave channel will be extinguished. This indicates which controls of the slave channel are still operative.</p>
