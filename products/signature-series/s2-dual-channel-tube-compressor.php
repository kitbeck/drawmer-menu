      <p><span class="style6"><span class="style5">S2 <span class="style5BLUE">- Dual Channel Tube Compressor</span></span></span></p>
      <p><span class="style1"> </span>The S2 Signature Series Dual Channel Tube Compressor is the latest product to emerge from Ivor Drawmer&rsquo;s high end designs and offers an &lsquo;all tube - no technical compromise&rsquo; circuit using only the highest grade components. The S2 features a host of new creative processing possibilities never before found in an all analogue dynamics package.</p>
      <p>&nbsp;</p>
      <p><strong>V-BIG</strong>- Retains bass frequencies and minimizes undesirable &lsquo;pumping&rsquo; by rolling off the detection signal at 75, 125 or 250Hz (user switchable). A fully variable level control allows for the desired amount of V-BIG processing and an in/out switch provides the option to remove from the signal path for A/B comparison.</p>
      <p>&nbsp;</p>
      <p><strong>V-AIR</strong>- A dynamic high frequency enhancer to keep compressed audio sounding fresh and bright with continuously variable frequency control (500Hz to 20kHz) and variable level to control the amount of V-AIR enhancement.</p>
      <p>&nbsp;</p>
      <p><strong>DRY</strong>- Mixes user defined amount of &lsquo;uncompressed&rsquo; signal with the compressed signal to create &lsquo;parallel compression effect&rsquo; without the need for external mixing devices.</p>
      <p>&nbsp;</p>
      <h3>Features:</h3>
      <ul class="feature">
	      <li>2 CHANNEL SOFT-KNEE TUBE COMPRESSOR.</li>
	      <li>FULLY BALANCED INTERNAL SIGNAL PATH.</li>
	      <li>CLASS A DESIGN.</li>
	      <li>10 (count them) VUCUUM TUBES PROVIDING 20 x ACTIVE TUBE STAGES.</li>
	      <li>ISOLATION TRANSFORMERS IN AND OUT.</li>
	      <li>&lsquo;ALLTUBE&rsquo; CIRCUIT DESIGN.</li>
	      <li>VARIABLE ATTACK AND RELEASE WITH OPTIONAL &lsquo;PROGRAMME DEPENDENT&rsquo; AUTO RELEASE OPERATION.</li>
	      <li>DUAL MONO OR STEREO LINK OPERATION.</li>
	      <li>SWITCHABLE &lsquo;PEAK&rsquo; OR &lsquo;VU&rsquo; METERING TO DISPLAY TRANSIENTS.</li>
	      <li>SWITCHABLE +10dB OR +20dB METER RE-SCALE MODES.</li>
	      <li>8 SEGMENT GAIN REDUCTION METERING.</li>
	      <li>&lsquo;V-BIG&rsquo; MODE FOR RETAINING LOW FREQUENCIES.</li>
	      <li>
	      &lsquo;V-AIR&rsquo; MODE FOR ENHANCING HIGH FREQUENCIES.</li>
	      <li>&lsquo;DRY&rsquo; MIX MODE FOR &lsquo;PARALLEL COMPRESSION EFFECT&rsquo;.</li>
	      <li>BALANCED XLR INPUTS/OUTPUTS.</li>
