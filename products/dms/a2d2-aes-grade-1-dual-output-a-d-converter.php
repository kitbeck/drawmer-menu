      <p><span class="style6"><span class="style5">A2D2 <span class="style5BLUE">- AES Grade 1 Dual Output A/D Converter</span></span></span></p>
      <p> <span class="style1">The A2D2 gives you high-quality A/D conversion</span> for any professional recording application. Microphones are analog, speakers are analog, storage is mostly a digital affair, and signal processing can reside in either the analog or digital realm, depending on whether the processing is software or hardware based. At some point in the signal path, you've got to get it to the digital world, and a high quality A/D converter is required - one which faithfully reproduces analog signals in the digital domain. The A2D2's Grade 1 AES-standard internal low-jitter clock generator and Burr Brown analog input stages ensure top-quality sound from the front end all the way through the subsequent conversion and processing. This means your signal is as pure as possible throughout the recording, editing, and mixing stages.</p>
      <p><br />
        The A2D2 is a stereo A/D converter giving simultaneous dual stereo outputs at different selectable sample rates from 44.1kHz to 192kHz. This makes it possible to have a main output sample rates of up to 192kHz with another running at 44.1kHz to serve as a low resolution copy. Automatic dither generation maintains signal purity and a linear power supply minimizes interference with the internal clocks. Three word clock outputs allow you to use the A2D2 as a master clock generator.</p>
      <p>&nbsp;</p>
      <h3>Features:</h3>
      <ul class="feature">
      	<li>Stereo A/D converter.</li>
      	<li>Simultaneous dual stereo outputs at different selectable sample rates from 44.1kHz to 192kHz.</li>
      	<li>Possible to have a main output at 192kHz with another at 44.1kHz as a low resolution copy.</li>
      	<li>Accurate 24 segment peak reading LED bar meters shows incoming signal level from -50dBfs to 0dBfs plus separate overload LEDs.</li>
      	<li>Dual input configuration allows either a fully variable input level from -2dBu to +28dBu via front panel rotary controls or 24 turn precision presets for a fully calibrated input.</li>
      	<li>Each digital output has selectable word length of 16 or 24 bits, with automatic dither generation.</li>
      	<li>The internal low jitter clock generator is Grade 1 AES standard.</li>
      	<li>External clock input.</li>
      	<li>3 word clock outputs to allow the A2D2 to act as master clock generator.</li>
      	<li>Burr Brown analogue input stages.</li>
      	<li>Linear power supply to minimise interference with the internal clocks.</li>
      </ul>
