      <p><span class="style6"><span class="style5">DS404 <span class="style5BLUE">- Quad Noise Gate</span></span></span></p>
      <p><span class="style1"> </span>The DS404 Quad Noise Gate has been designed to the highest specification to complement the DS201 Dual Noise Gate, acknowledged as the 'industry standard' gate throughout the world. Recognizing that there are many general gating applications which require equipment that is simple to operate and which occupies the minimum of rack space, Drawmer have designed the DS404 Quad Noise Gate utilizing new and unique 'Programme Adaptive' circuitry. This makes the DS404 ideally suited for use over a wide range of input signals, ranging from drums and other percussive instruments through to vocals, pianos and even complete mixes.</p>
      <p>&nbsp;</p>
      <p>Facilities available on the DS404 include:      
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Quad gate </strong>: Four channels of DS201 quality noise gates.      
      <p><br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Frequency Sensitive </strong>: The provision of variable Low-Pass and High-Pass Filters allows 'without compromise' frequency selective gating. The unit can be switched for either Internal or External Key source and the <em>KEY LISTEN</em> facility enables monitoring of the filter setting.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Hard/Soft Gating :</strong> Each channel of the DS404 can be operated as a HARD or SOFT gate, providing quite different characteristics. Two coloured LEDs clearly indicate mode status. In the HARD mode the DS404 offers ultra-fast response time, stable triggering with complete freedom from chatter around threshold and a specialized release contour which is ideally suited to drums and other percussive material. In the SOFT mode the DS404 becomes a versatile Expander capable of handling vocals and sub mixes. Drawmer's unique 'Programme Adaptive' circuitry is used to optimise the attack time and ratio as well has having some effect on the manual release setting . A more gentle release characteristic is utilized to complement the SOFT gate.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Threshold :</strong> A fully variable threshold control provides a range from +20dB to -70dB.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Release Control :</strong> A fully variable release control is provided with times from 10 ms to 5 sec to ensure that essential envelope control is not sacrificed in exchange for an economy in rack space.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Attenuation Range :</strong> The DS404 has switchable attenuation between 90dB and 20dB to allow for applications where a complete mute is not desired.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Chain Linking :</strong> Operating the LINK control switches the channel on the right to SLAVE operation, where its gating mode, threshold and envelope characteristics are controlled by the MASTER (left) channel. Any adjacent three channels or two stereo pairs can be linked in this way for use in any conceivable situation where linking may be required.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>LED Displays </strong>: Drawmer's familiar tri-colour LED display is once again utilized as the optimum method for indicating the envelope status for each channel.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Input/Output :</strong> The DS404 is provided with balanced inputs and outputs on XLR connections.</p>
