      <p><span class="style6"><span class="style5">DL251 <span class="style5BLUE">- Spectral Compressor</span></span></span></p>
      <p><span class="style1"> </span> It is well known that compression, in its basic form, tends to remove high frequencies from any material with a wide bandwidth. This is because material usually has less energy at high frequencies than it does at low frequencies and since single band compressors are not frequency conscious, the 'highs' are attenuated together with the 'lows'. Using a slow attack time improves matters to some extent, but signals containing many harmonics (eg: vocals, strings and brass) or dynamic signals with a powerful 'leading edge' (eg: piano, kick and snare drum) tend to lose some clarity which often requires further audio processing. It is a fact that many engineers who would like to use a compressor in a particular application, avoid doing so because they feel that the 'sound loses something'. The DL251 'Spectral Compressor', incorporates a variable 'Dynamic Spectral Enhancement' (D.S.E.) section which eliminates the problems associated with full band compressors by restoring the high frequency energy lost during the full band compression process. This enhancement circuitry is extremely effective where compression and limiting are employed for broadcast transmission, or across complete mixes in a stereo mixdown situation. 'Dynamic Spectral Enhancement' has many creative applications when recording individual components at the multi-track stage, particularly on such programme as acoustic guitar, piano, brass and vocals.</p>
      <p>&nbsp;</p>
      <p>Facilities available on the DL251 include:<br />
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Compressor </strong>: The compressor section can be switched to provide either HARD or SOFT knee compression. In both the HARD and SOFT modes a Ratio control is available. In the SOFT mode the ratio increases gradually over a 10dB input level range until it reaches the ratio set on the front panel control, thus achieving a more natural sounding compression. Selectable manual or automatic Attack and Release times dramatically extend the application range and user scope of the DL251. Selecting the 'Auto' mode removes the need for manual adjustment and is particularly valuable when the programme being treated does not have predictable or consistent dynamics, such as on complete mixes, vocals or 'slapped and pulled' bass guitar. Under manual control the wide range of adjustment available gives the user the ability to create dynamic effects as well as normal compression.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Enhance Level :</strong> A variable Enhance control can be used to 'dynamically' boost any high frequency energy lost during the full band compression process and has sufficient range to add additional 'Spectral Energy' to the programme material for creative processing. When no compression is taking place, the 'Dynamic Spectral Enhancement' circuitry is inoperative and so no change is made to the signal. However, as the signal level rises to the point where gain reduction is taking place, the Enhancement system 'kicks in' applying dynamic boost which is directly related to the amount of gain reduction taking place. A front panel switch offers an instant A/B comparison of this feature and a green LED indicates the amount of enhancement taking place.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Metering :</strong> Comprehensive bar graph displays give 'at a glance' indications of gain reduction, and input/output signal level Gain reduction from 1 dB to -30dB is shown simultaneously with signal level from 20dB to + 15dB Other single LEDs indicate function status of Hard/ Soft compression, Auto Attack/ Release, Side chain monitoring, Bypass, Enhancement, Peak Limiting, Stereo Linking and Power on.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Side Chain Listen </strong>: A front panel push button with LED status enables the monitoring of any external processing applied to the side chain access connections on the rear panel.</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" />Bypass : A fully balanced hard wire bypass connects the input directly to the output allowing signal to pass through the unit with no power applied. In bypass mode the output level bar graph displays the Input signal.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Stereo Link </strong>: When processing stereo signals, linking should be used to prevent image shifting. This facility ties both channels together. Using channel 1 (Left) as the master channel removes the need to duplicate control settings for both channels. A rear panel switch allows the selection of 'Peak' or 'Average' stereo signal detection.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Peak Level :</strong> 'Zero Response Time', 'Zero Overshoot' circuitry provides an absolute limit to the peak level of the output signal, adjustable from 0dB to +16dB above system level When set to the required level, any excessive peaks will be stopped and the gain momentarily decreased if necessary The 'Zero Response Time' is essential to avoid unpleasant distortion when mastering for CD or sending any signal to digital equipment. 'Zero Response Time' means that the DL251 provides complete protection from even the fastest and shortest signal 'spikes' (overloads), making the unit also ideally suited for speaker system protection in sound reinforcement applications.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Operating Level :</strong> The operating level of the DL251 can be instantly switched, independently on each channel, for either +4dBu or -10dBu operation using a rear panel push switch.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong>Input/Output :</strong> The DL251 is provided with balanced inputs and outputs on XLR connections.</p>
