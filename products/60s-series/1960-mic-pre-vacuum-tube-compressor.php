      <p><span class="style6"><span class="style5">1960 <span class="style5BLUE">- Mic Pre / Vacuum Tube Compressor</span></span></span></p>
      <p><span class="style1"> </span>There is little doubt in the minds of many engineers and producers that vacuum tubes possess a unique sound quality providing a &quot;warmer, more alive&quot; sound. Combining eight active tube stages and the low noise and reliability of solid state electronics, the Drawmer 1960 provides the ultimate direct interface between the sound source and the recording medium. The Drawmer 1960 is a two channel unit with each channel comprised of a completely natural sounding &quot;soft knee&quot; vacuum tube compressor and an extremely low noise microphone pre-amplifier with switchable 48 volt phantom power. A further auxiliary instrument pre-amp with EQ is also provided. This powerful combination of the classic valve circuit, offering unsurpassed tonality, and the low noise balanced microphone input with up to 60dB of gain has allowed engineers throughout the world to exploit the full potential of high performance condenser microphones. An example of this would be the direct to digital stereo recording of live classical music without the necessity of a mixing console.</p>
      <p>&nbsp;</p>
      <p>The Compressor: Because the compressor is a soft knee type, it requires few controls. Strictly speaking, a soft knee design does not have a rigidly defined threshold but the title 'Threshold' has been retained for the &quot;amount of compression&quot; control for the sake of familiarity. The Threshold range is continuously variable between infinity and -24dB, the theory being that signals below the threshold are essentially unprocessed and the signals exceeding this threshold are subjected to increasing amounts of gain reduction dependent on by how much they exceed the threshold.<br />
        The attack and release times are switchable rather than being continuously variable and this method of operation falls into line with that of its all valve predecessors. Attack simply offers a choice of slow, medium and fast whilst release has six settings. Positions one to four have fixed release times whilst positions five and six are two different programme dependent release time modes making the unit ideally suited to programme material with complex dynamics such as complete stereo mixes and vocals. Interestingly, the attack times are further modified by the choice of release setting so the range of choices are wider than it might first appear.</p>
      <p>&nbsp;</p>
      <p>Because compression is a form of controlled gain reduction, a stage of make-up amplification follows the compressor section giving a range of +20dB. The VU meters may be switched to monitor either the amount of gain reduction taking place or the output signal level and the two channels may be used either independently or as a stereo pair, depending on the setting of the stereo link switch.</p>
      <p>The side chain access points may be used to patch in equalisers to enable the compressor to act as a de-esser. As with other Drawmer products, the by-pass switch offers a choice of Normal, By-pass and Side Chain Listen modes so that if you have patched in any additional side chain processing, you can easily monitor what effect it is having.</p>
      <p>&nbsp;</p>
      <p>In addition to the balanced microphone and line inputs on the rear of the unit an auxiliary input on the front panel gives direct access for guitars or electronic instruments. Equalization is provided along with sufficient gain to overload the tube pre-amp for sustain effects. The Aux input is a valuable addition as it combines the virtues of a D.I. box and equalizer with the compressor allowing the instrument to be simultaneously processed and routed to the mixing console or directly to the recorder. </p>
