      <p><span class="style6"><span class="style5">1961 <span class="style5BLUE">- Vacuum Tube Equaliser</span></span></span></p>
      <p>It has long been recognized that vacuum tubes impart a unique 'warmth' and 'musicality' to an audio signal, but many of the 'classic' tube equalizers fall short of the technical performance demanded in today's digital recording age. Designed to complement the widely acclaimed Drawmer 1960 Mic Pre-Amp Vacuum Tube Compressor, the 1961 Vacuum Tube Equalizer combines low noise circuitry with no fewer than twelve active tube stages to provide two channels of exceptional equalization possessing unique 'personality'. Rather than employ a continuously variable resistor as a means of frequency control, the 1961 utilizes the same rotary 'step' switching system used in vintage designs . This enables the component values for each frequency band to be optimized for uncompromising performance across the entire audio spectrum and also makes setting up more accurate, particularly when treating stereo signals.</p>
      <p>&nbsp;</p>
      <p>The design of the 1961 allows the user to control the amount of 'warmth' by deciding how hard to drive the tubes giving a sound as 'clean' as the best discrete solid state designs or as 'hot' as the 'hottest' classic tube designs. The 1961 and 1960 Can be 'interlinked' to provide, in effect, a two channel vacuum tube mixing console, - the perfect mastering or post audio 'sweetening' tool.</p>
      <p>&nbsp;</p>
      <p><strong>Features include:</strong></p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong> Input level : </strong></p>
      <p>A variable input level of -20dB to +20dB ensures compatibility with a wide range of input source material and enables the user to optimize signal level and drive the tubes 'soft' or 'hot' as required. LED bargraph displays give a clear indication of the input signal level.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /> <strong>High Pass Filter :</strong> </p>
      <p>A variable 12dB/Octave roll-off filter adjustable from - 15Hz to 500Hz section enables the user to remove unwanted low frequencies and sub-sonic noise.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /> <strong>Equalization :</strong> </p>
      <p>Each channel of the 1961 has four main equalizer sections with six carefully chosen overlapping frequencies and a separate tube stage for increased harmonic clarity. Each section also includes variable band width from 0.3 Octave to 3 Octaves and +18dB of Cut and Boost. BASS frequencies (Hz) 20, 32, 50, 80, 125, 200. LO-MID frequencies (Hz) 100, 180, 250, 400, 650, 1K. HI-MID frequencies (kHz) 0.5, 0.8, 1.2, 2.0, 3.2, 5.0. TREBLE frequencies (kHz) 2.5, 4, 6,10,15, 25.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /> <strong>Low Pass Filter :</strong> </p>
      <p>A variable 12dB/Octave roll-off filter adjustable from 2.5kHz to 56kHz enables the user to remove high frequency interference or to band limit excessively high levels of high frequencies.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /> <strong>Output Stage :</strong> </p>
      <p>Two additional tube stages in the output section of each channel allow the user to achieve 'soft clipping' by adjusting the input level control whilst visually monitoring the effect upon the 'Soft' and 'O/L' (Overload) LED's.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /> <strong>Bypass :</strong> </p>
      <p>In addition to a master bypass switch, each of the parametric/shelving filters has its own separate bypass switch, making the 1961 very straight forward to set up. It allows instant A-B comparison and also enables the tube output stages to be used to add tube coloration without EQ simply by leaving each channel switched to 'In' and each individual EQ sections switched to 'Out'. The master bypass switch for channel bypasses all the filter sections as well as the output stage vacuum tube circuitry.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /> <strong>Operating Level :</strong></p>
      <p> Levels of -10dBu and +4dBu are selectable using a rear panel switch.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong> Insert Connections :</strong> </p>
      <p>Insert point 1/4&quot; jack connectors on the rear panel enable direct connection to the Side chain or Insert points of the Drawmer 1960 Mic Pre-Amp/ Vacuum Tube Compressor. The 1961 and 1960, when 'interlinked', produce a powerful package for recording signals direct to tape; gain control, EQ &amp; compression can be applied to mic/line level signals without having to go via a mixing console.</p>
      <p>&nbsp;</p>
      <p><img src="../../images/spacer_dot.jpg" alt="" width="25" height="9" /><strong> Inputs and outputs : </strong></p>
      <p>The 1961 is provided with balanced inputs and outputs on XLR connections.</p>
