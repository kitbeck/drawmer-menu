      <p><span class="style6"><span class="style5">ARCHIVED PRODUCTS</span></span></p>
      <p>&nbsp;</p>
      <p><span class="style1"> </span>Drawmer products are built to the highest standards and so it's not unusual to find our older products that are still in use even though they were originally sold thirty years ago. Here you will find information and operator's manuals for products that we no longer manufacture.</p>
            <p>&nbsp;</p>
            <p><span class="style6"><span class="style5"><a name="dsl424" id="dsl424"></a>DSL424 <span class="style5BLUE">- TwoPlusTwo Dynamics</span></span></span></p>
      <p>&nbsp;</p>
      <p>  <span class="style1">The DSL424 provides the engineer with a 1U four channel comprehensive toolbox capable of solving the most complex dynamic problems.</span> Incorporated in the 1U package are two industry standard frequency conscious noise gates and two soft/hard knee compressors with variable threshold limiting. The channels may be front panel configured as four individual stand alone processors, a stereo linked pair of comp/limiters with a stereo linked pair of gates. Alternatively, any combination of processing can be achieved by rear panel patching.</p>
      <p>&nbsp;</p>
      <p>The DSL424 combines the gates of the DS404 with the compressor/limiters of the DL441 to provide a versatile 1U problem solver invaluable in applications where rack space is limited.</p>
      <p>&nbsp;</p>
      <p>Recognizing that there are many general applications which require equipment that is simple to operate, Drawmer have designed the noise gate stages of the DSL424 utilizing &lsquo;Programme Adaptive&rsquo; circuitry. This makes the DSL424 ideally suited for use over a wide range of input signals, ranging from drums and other percussive instruments through to vocals, pianos and even complete mixes.</p>
      <p>&nbsp;</p>
      <p>The provision of variable Low-Pass and High-Pass Filters allows &lsquo;without compromise&rsquo; frequency selective gating with a KEY LISTEN facility enabling monitoring of the filter setting. </p>
      <p>&nbsp;</p>
      <p>Each gate channel can be operated as HARD - offering ultra-fast response time, stable triggering and a specialized release contour which is ideally suited to percussive material or SOFT - a versatile Expander capable of handling vocals and sub mixes.</p>
      <p>&nbsp;</p>
      <p><strong>Noise Gate</strong><br />
      <p>Variable High/Low Pass Filters for frequency triggering.<br />
      <p>Auto attack.<br />
      <p>Variable release.<br />
      <p>Switchable range with LED status.<br />
      <p>Selectable &lsquo;Hard&rsquo; (drums etc.) or &lsquo;Soft&rsquo; (vocals etc.).<br />
      <p>1/4 inch jack Key Inputs.<br />
      <p>Key Listen facility.<br />
      <p>Slave linking.</p>

      <p>&nbsp;</p>
      <p><strong>The compressor section</strong> features a highly developed, auto attack/release system which adapts itself to the programme material being processed and is equally effective on individual sounds or complex mixes.</p>
      <p>&nbsp;</p>
      <p>To maximise flexibility, each channel of the DSL424 is switchable between traditional ratio (hard knee) and soft-knee operation. Traditionally, soft-knee compressors are preferable for unobtrusive level control or for the control of finished mixes, whereas ratio type compressors are generally considered more successful in creative applications or where large amounts of gain reduction are required. By offering a choice of both modes, the DSL424 is capable of outstanding results in a very wide range of studio and live sound situations. The two channels may be operated independently or linked for true stereo operation.</p>
      <p>&nbsp;</p>
      <p>Also included in each channel of the DSL424 is a peak limiter which allows the user to set an absolute output signal level that will not be exceeded. If the peak limiter threshold is exceeded for more than a few milliseconds, additional gain reduction will be applied to reduce the overall signal level to within accepted limits without distortion. Once the peak has passed, the system gain will return to normal after a period of about one second. This facility is extremely valuable both in live sound applications, for driver protection, and in digital recording where an absolute maximum recording level exists. Furthermore, when deliberately overdriven, it can be used creatively to produce level &ldquo;pumping&rdquo; effects which can be useful on electric guitar or rock vocal sounds.</p>
      <p>&nbsp;</p>
      <p><strong>Compressor/Limiter</strong><br />
      <p>Switchable Hard/Soft knee compression with variable threshold, ratio and output gain.<br />
      <p>Auto attack/release.<br />
      <p>Variable &lsquo;zero overshoot&rsquo; transparent limiter.<br />
      <p>High resolution bargraph metering of gain reduction and input/output level.<br />
      <p>Stereo linking.<br />
      <p>Balanced +4dB XLR in/out.</p>
