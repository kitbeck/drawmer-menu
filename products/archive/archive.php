      <p><span class="style5"><a name="dmt1080" id="dmt1080"></a>DMT1080 <span class="style5BLUE">- Dual Multi-Tracker</span></span></p>
      <p>&nbsp;</p>
      <p>The first Drawmer product, very rare and highly prized. Prospective purchasers should check that the unit is in good working order. Restoration or repair could prove very expensive.
      
      <p>The Drawmer DMT 1080 contains two independant delay lines. A multi section delay having a maximum delay time of 80mS, has taps at 10mS, 30mS, 50mS and 80 mS which are fed to either channel 1 or channel 2 by the touch button selectors. The same tap on both channels produces a mono output whilst different taps produce stereo images. Chorus on either channel produces two delays, each of which is differant from the main 10,30,50,80mS delays and the two chorus delays on the other channel; so chorus on both channelsgives a stereo image.</p>
      <p>The output from an auxilliary 10mS delay is fed via the 'Phase' level control to both channels giving a mono image. A clean feed signal is connected in a similar manner via the 'Feedthrough' level control.</p>
      <p>Delay time is variable over a 30:1 ratio either manually ('Manual Delay') or automatically using the internal low frequency oscillator which runs at 0.05Hz to 15Hz.</p>
      <p>Control Voltages for each delay line are in anti-phasei.e. when the main delay is at maximum, the 'Phase' delay is at minimum and vice versa. This means that true phasing can occur at several points in the sweep (from min to max delay) as each main delay tap in turn cancels the signal from the auxilliary delay.</p>
      <p>Outputs from the main delay taps are of fixed level. They are mixed with the variable 'Phase' and 'Feedthrough' signals before the output level controls. + or - signals from channel 1 can be routed back via the feedback control to the delay line inputs. +feedback produces resonant peaks at short delays whilst - feedback gives a notch filter type of response.</p>
