             <p><span class="style5"><a name="mclock" id="mclock"></a>M-Clock <span class="style5BLUE">- Master Clock Generator and Multiple Sample Rate Converter</span></span></p>
      <p>&nbsp;</p>
      <p>In today's studio where increasing numbers of digital devices need to be interconnected, maintaining digital signal integrity can be a serious problem. The traditional professional solution has been to use an expensive master clock that drives all the digital equipment in the studio, but in many different types of studios there are often pieces of equipment that don't have wordclock inputs such as consumer CD players, MD recorders, DAT machines and budget computer soundcards.
      <p>Drawmer's unique solution to this dilemma is the M-Clock, an affordable yet extremely highly specified, multiple output master clock generator that also incorporates four further channels of sample-rate-conversion, all locked to the same master clock. There are also alternative sync outputs for those pieces of equipment without wordclock connections that can be sync'ed via their digital audio inputs using a 'black' signal carrying no audio information.</p>
      <p>The advantages of combining wordclock with multiple channels of 'clock-locked' sample rate conversion are significant and often under-appreciated. With M-Clock, all your 'pro' and consumer digital equipment can run in the same system, locked to the same clock, with no worries about finding a suitable sync source. You'll find you're no longer having to switch equipment between internal and external sync as you repatch - and the M-Clock's  ultra-precision, low-jitter clock helps maintain optimum stereo imaging, lower noise and lower distortion. If you have multiple pieces of digital equipment in your studio, whether professional or a mixture of professional and consumer, M-Clock is probably the most effective upgrade you can make for anything like the price.</p>
      <p>&nbsp;</p>
      <p>The M-clock has since been replaced by the <a href="../dms/m-clock-plus-master-clock-src.php">M-Clock Plus</a> and <a href="../dms/m-clock-lite-aes-grade-1-master-clock.php">M-Clock Lite.</a></p>
