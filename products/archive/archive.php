      <p><span class="style6"><span class="style5"><a name="3sum" id="3sum"></a>Three-Sum <span class="style5BLUE">- Multi-Band Interface</span></span></span></p>
       <p>&nbsp;</p>
      <p><span class="style1"> </span>The benefits of multi-band processing, with the ability to apply different parameters to different sections of the audio bandwidth have been apparent to sound engineers for quite some time. However, &lsquo;locking in&rsquo; your audio to any one specific multi-band unit can vastly reduce the creative options. The desired combination of processing to deal with low frequency energy, the vocal mid range and high frequency detail and enhancement is not necessarily available from any one manufacturer.</p>
      <p>The new Drawmer THREE-SUM opens up a whole new set of options allowing the engineer to split the audio into two or three bands and apply his or her own sonic signature to each part of the audio bandwidth. The THREE-SUM employs a high quality signal path culminating in a variable threshold, brick wall limiter section with bypass facility. To ensure transparency the limiter design is &lsquo;two stage&rsquo;, applying different processing to the H.F. content of the material. In applications where the dynamics of the material need to be retained to create an open sound, the limiter is essential for catching peaks.</p>
      <p>&nbsp;</p>
      <p>The THREE-SUM has been designed for use in high-end mastering and general recording applications, alternatively, it can transform a project studio, equipped only with &lsquo;single band&rsquo; processors, into a serious multi-band facility.</p>
      <p>&nbsp;</p>
      <p><strong>Main Features</strong><br />
      <p>Continuously Variable Band Splitting.<br />
      <p>Switchable Mute &amp; Bypass On Each Band.<br />
      <p>Variable Threshold, Two Stage Brick Wall Limiter Section.<br />
      <p>Two Or Three Band Operation.<br />
      <p>VU Metering With +10dB Re-Scale Mode For Hot Output Levels.<br />
      <p>Variable Input &amp; Output Levels.<br />
      <p>Individual Band Or Multi-Band Processing.<br />
      <p>High Quality, Minimal Signal Path With Balanced XLR Inputs/Outputs.</p>
