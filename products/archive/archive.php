      <p><span class="style5"><a name="df330" id="df330"></a>DF330 <span class="style5BLUE">- Universal Noise Filter</span></span></p>
      <p>&nbsp;</p>
      <p>The Drawmer DF330 is a single ended noise reduction system which does not require any previous encoding. Instead, it uses a dynamic filter which senses the highest frequency of incoming program material and opens a filter to that frequency. It has been designed to dramatically reduce noise, regardless of source, in a wide variety of recording/broadcast situations. An example of this is when old recordings are remastered for CD. Some other applications would include: Analogue to digital mastering, reducing tape hiss and noise resulting from high gain and high frequency boost on mixing consoles. Restoring degraded or poorly recorded material for tape duplication and resolving problems with noisy synthesizers, guitar amplifiers, effects units etc.
      
      <p>The main features of the Drawmer DF330 include:</p>
      <p> Excellent visual displays are provided. The input level is displayed with a 3 colour 'traffic light' style LED meter. The filter cut-off frequency display has nine LED segments and the expander gain reduction has a five segment LED display. Further to this there are LED's to show bypass, stereo link status and power input<br />
        A 50Hz 'rumble' filter with a slope of 18dB per octave is available independently on both channels<br />
        In addition to a full system bypass, the expander and filter can be switched out of the audio path separately, enabling easy comparisons to be made. under all conditions<br />
        A comprehensive expander with automatic attack time, variable release and switchable ratio and attenuation gives up to 40dB reduction in overall noise during silent passages<br />
        Stereo linking allows both channels to track identically. This prevents image shifting with stereo programme, and phase shifts when both channels are mixed into mono<br />
        The dynamic filter has two modes of operation. In the manual mode, the threshold is adjusted by the user to give optimum results. In the Auto mode the unit is capable of tracking the signal level thereby enabling the filter to continue to operate successfully during fade ups and downs. This makes manual adjustment unnecessary<br />
        Simultaneous -10dBu unbalanced 1/4 inch jack and +4dBu balanced XLR inputs and outputs allows the unit to be used as a level converting interface between the two operating levels, e.g. The output from a cassette machine at -10dBu could give a balanced +4dBu output and -10dBu output</p>
