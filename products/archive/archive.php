      <p><span class="style5"><a name="e101" id="e101"></a>E101 <span class="style5BLUE">- Passive E.Q.</span></span></p>
      <p>&nbsp;</p>
      <p>The Drawmer E101 was a four stage, single channel passive equaliser, followed by a stage of makeup gain to make up for the insertion loss of passive circuitry. Traditional Inductor/ Capacitor/ Resistor circuits are employed in the filter design and a very low noise, solid state amplifier is used to provide the necessary gain. This in our view, gave the best of both worlds; the smoothness of purely passive filtering combined with the low noise, low distortion amplification which can only be accomplished using recent technology. The format of the Drawmer E101 offered a high degree of control at the extreme of the audio spectrum, where precise correction or subtlle enhancement is often necessary.
      <p>Because the Drawmer E101 used a passive equaliser circuit, seperate filter sections were provided for cut and boost. This had the advantage of allowing the user to simultaneously add cut and boost at different frequencies. With all the cut and boost controls set to flat (fully anti-clockwise), the equaliser had unity gain.</p>
      <p>There are four equaliser sections, two for the bass end of the audio spectrum and two for the treble. Frequancy selection is by means of stepped rotary switches while the cut and boost controls are continuously variable.</p>
      <p> BYPASS switches the EQ and make-up gain amplifier out of circuit.</p>
      <p> BASS CUT The Bass Cut section employs a shelving Hi-pass filter which may be switched to operate at : 20Hz, 30Hz, 60Hz, or 100Hz. Up to 15dB of cut may be applied and this section is useful for attenuating frequencies below the useful range of the programme being processed or for reducing overall boominess.</p>
      <p> BASS BOOST Switched to operate at : 20Hz, 30Hz, 60Hz, or 100Hz., the bass boost section offers a choice of Hi-pass shelving or peaking characteristics. In 'Peak' mode the 'Q' of the filter is 2.5, and the maximum boost is 16dB. In 'Shelf' mode the maximum boost is 13.5dB.</p>
      <p> TREBLE BOOST the Treble Boost section comprises a variable 'Q' peaking filter operatring at frequencies of 3KHz, 4KHz, 5KHz, 10KHz, and 16KHz. The bandwith control allows adjustment of 'Q' over the range 0.8 to 2.1. Up to 12 dB of boost may be applied at the minimum 'Q' setting, and up to 18dB of boost are available at the maximum'Q' setting.</p>
      <p> TREBLE CUT This section has a shelving Lo-pass characteristic and may be switched to operate at 5KHz, 10 KHZ or 20KHz. A maximum of 18dB of cut may be applied. Apart from its tonal applications, this section is useful for attenuating frequencies above the natural range of the programme being equalised which will also serve to attenuate any high frequency noise present in the source programme.</p>
